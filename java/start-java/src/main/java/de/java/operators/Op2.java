package de.java.operators;

public class Op2 {
    public static void main(String[] args) {

        int a = 20;
        int b = 10;
        int c = 20;

        // 숫자 비교
        System.out.println("a = 20, b = 10, c = 20");
        System.out.println("a == b returns " + (a==b));
        System.out.println("a != b returns " + (a!=b));
        System.out.println("a > b returns " + (a>b));
        System.out.println("a >= c returns " + (a>=c));
        System.out.println("a < b returns " + (a<b));
        System.out.println("a <= b returns " + (a<=b));

    }
}
