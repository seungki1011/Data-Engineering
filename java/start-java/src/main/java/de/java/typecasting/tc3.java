package de.java.typecasting;

public class tc3 {
    public static void main(String[] args) {
        double d1;
        d1 = 10 / 4;
        System.out.println("d1 = " + d1);
        d1 = 10.0 / 4.0;
        System.out.println("d1 = " + d1);
        d1 = (double) 10 / 4;
        System.out.println("d1 = " + d1);
        d1 = 10.0 / 4;
        System.out.println("d1 = " + d1);
    }
}
